function cambiarColor(color){

    if (color==='Azul'){
        document.getElementById('texto').style.backgroundColor="#0000FF";
        
    }
    
    else if (color==='Verde'){
        document.getElementById('texto').style.backgroundColor="#00FF00";
        
    }
    
    else {
        document.getElementById('texto').style.backgroundColor="#FFFFFF";
        
    }
}